#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t nb)
{
	int		i;
	int		j;
	size_t	c;

	i = 0;
	j = 0;
	c = 0;
	while (s1[i] != '\0')
		i++;
	while ((s2[j] != '\0') && (c < nb))
	{
		s1[i] = s2[j];
		i++;
		j++;
		c++;
	}
	s1[i] = '\0';
	return (s1);
}
